const eqArrays = function(arr1, arr2) {
  if (arr1.length !== arr2.length) {
    return false;
  }
  for (let x = 0; x < arr1.length; x++) {
    if (arr1[x] !== arr2[x]) {
      return false;
    }
  }
  return true;
};

const takeUntil = function(array, callback) {
  let result = [];
  for (let i = 0; i < array.length; i++) {

    if (!callback(array[i])) {
      result.push(array[i]);
    } else {
      return result;
    }
  }
  return result;
};

